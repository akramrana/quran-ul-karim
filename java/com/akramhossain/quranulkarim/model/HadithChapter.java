package com.akramhossain.quranulkarim.model;

public class HadithChapter {

    private String bid;

    private String name_bangla;

    private String name_english;

    private String name_arabic;

    private String reference_book;

    private String kitab_id;

    private String name_slug;

    public String getBid() {
        return bid;
    }

    public void setBid(String bid) {
        this.bid = bid;
    }

    public String getName_bangla() {
        return name_bangla;
    }

    public void setName_bangla(String name_bangla) {
        this.name_bangla = name_bangla;
    }

    public String getName_english() {
        return name_english;
    }

    public void setName_english(String name_english) {
        this.name_english = name_english;
    }

    public String getName_arabic() {
        return name_arabic;
    }

    public void setName_arabic(String name_arabic) {
        this.name_arabic = name_arabic;
    }

    public String getReference_book() {
        return reference_book;
    }

    public void setReference_book(String reference_book) {
        this.reference_book = reference_book;
    }

    public String getKitab_id() {
        return kitab_id;
    }

    public void setKitab_id(String kitab_id) {
        this.kitab_id = kitab_id;
    }

    public String getName_slug() {
        return name_slug;
    }

    public void setName_slug(String name_slug) {
        this.name_slug = name_slug;
    }
}
