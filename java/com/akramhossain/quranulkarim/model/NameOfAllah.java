package com.akramhossain.quranulkarim.model;

public class NameOfAllah {

    private String name;
    private String transliteration;
    private String number;
    private String found;
    private String en_meaning;
    private String en_desc;
    private String bn_meaning;
    private String bn_desc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTransliteration() {
        return transliteration;
    }

    public void setTransliteration(String transliteration) {
        this.transliteration = transliteration;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getFound() {
        return found;
    }

    public void setFound(String found) {
        this.found = found;
    }

    public String getEn_meaning() {
        return en_meaning;
    }

    public void setEn_meaning(String en_meaning) {
        this.en_meaning = en_meaning;
    }

    public String getEn_desc() {
        return en_desc;
    }

    public void setEn_desc(String en_desc) {
        this.en_desc = en_desc;
    }

    public String getBn_meaning() {
        return bn_meaning;
    }

    public void setBn_meaning(String bn_meaning) {
        this.bn_meaning = bn_meaning;
    }

    public String getBn_desc() {
        return bn_desc;
    }

    public void setBn_desc(String bn_desc) {
        this.bn_desc = bn_desc;
    }
}
